// ── 数据结构 ──────────────────────────────────────────────────────────────────

type SubscriptionGroup struct {
	ID        uint   `json:"id"`
	Name      string `json:"name"`
	Token     string `json:"token"`
	NodeCount int    `json:"node_count"`
	CreatedAt string `json:"created_at"`
	UpdatedAt string `json:"updated_at"`
}

type CreateGroupRequest struct {
	Name string `json:"name"`
}

type SetGroupNodesRequest struct {
	Nodes []string `json:"nodes"`
}

// ResetTokenResponse is the JSON body for POST /admin/subscription-groups/{id}/reset-token.
type ResetTokenResponse struct {
	Token string `json:"token"`
}

// ── 工具函数 ──────────────────────────────────────────────────────────────────

func generateToken() (string, error) {
	b := make([]byte, 32)
	if _, err := rand.Read(b); err != nil {
		return "", err
	}
	return hex.EncodeToString(b), nil
}

// queryNodesByMACs 按 MAC 列表查询节点，保持顺序。
func queryNodesByMACs(macs []string) ([]Node, error) {
	if len(macs) == 0 {
		return nil, nil
	}
	placeholders := strings.Repeat("?,", len(macs))
	placeholders = placeholders[:len(placeholders)-1]
	args := make([]interface{}, len(macs))
	for i, m := range macs {
		args[i] = m
	}
	rows, err := db.Query(
		"SELECT "+nodeCols+" FROM nodes WHERE mac IN ("+placeholders+") ORDER BY hostname",
		args...,
	)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var nodes []Node
	for rows.Next() {
		n, err := scanNode(rows)
		if err != nil {
			return nil, err
		}
		nodes = append(nodes, n)
	}
	return nodes, nil
}

// buildGroupClashYAML 生成分组专属的 Clash YAML，复用 subscription.go 的工具函数。
func buildGroupClashYAML(groupName string, nodes []Node) string {
	var proxyNames []string
	var proxies strings.Builder

	for _, n := range nodes {
		host := stripScheme(n.CFURL)
		if host == "" || n.XrayUUID == "" {
			continue
		}
		name := buildNodeName(n)
		proxyNames = append(proxyNames, "    - "+yamlStr(name))
		fmt.Fprintf(&proxies, `  - name: %s
    type: vless
    server: %s
    port: 443
    uuid: %s
    network: ws
    tls: true
    servername: %s
    ws-opts:
      path: /%s
      headers:
        Host: %s
      max-early-data: 2560
      early-data-header-name: Sec-WebSocket-Protocol
`, yamlStr(name), host, n.XrayUUID, host, xrayPath, host)
	}

	autoName := groupName + "-AUTO"
	selectName := groupName + "-SELECT"

	yaml := &strings.Builder{}
	fmt.Fprintf(yaml, "# Hive Node Registry - %s\n", groupName)
	fmt.Fprintln(yaml, "# Generated by hive-registry")
	fmt.Fprintln(yaml, "")
	fmt.Fprintln(yaml, "proxies:")
	fmt.Fprint(yaml, proxies.String())
	fmt.Fprintln(yaml, "")
	fmt.Fprintln(yaml, "proxy-groups:")
	fmt.Fprintf(yaml, "  - name: %s\n", autoName)
	fmt.Fprintln(yaml, "    type: url-test")
	fmt.Fprintln(yaml, "    url: http://www.gstatic.com/generate_204")
	fmt.Fprintln(yaml, "    interval: 300")
	fmt.Fprintln(yaml, "    proxies:")
	for _, name := range proxyNames {
		fmt.Fprintln(yaml, name)
	}
	fmt.Fprintln(yaml, "")
	fmt.Fprintf(yaml, "  - name: %s\n", selectName)
	fmt.Fprintln(yaml, "    type: select")
	fmt.Fprintln(yaml, "    proxies:")
	fmt.Fprintf(yaml, "    - %s\n", autoName)
	for _, name := range proxyNames {
		fmt.Fprintln(yaml, name)
	}
	fmt.Fprintln(yaml, "")
	fmt.Fprintf(yaml, "rules:\n  - MATCH,%s\n", selectName)

	return yaml.String()
}

