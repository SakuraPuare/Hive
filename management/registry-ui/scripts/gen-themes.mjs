/**
 * gen-themes.mjs — 构建期主题色生成器
 * ---------------------------------------------------------------------------
 * 用官方 @material/material-color-utilities 把每个 Material Design 调色板种子色
 * 算成一整套 Material 3 角色（primary/secondary/tertiary/error + 8 级 surface
 * container + outline/inverse 等），明暗双模各一份,输出为 HSL 三元组 CSS 变量。
 *
 * 产物 styles/themes.css 用 `[data-theme-color="<key>"]` 属性选择器承载,unlayered
 * (在 @layer 之外),因此优先级高于 globals.css 里 @layer base 的默认 Google 蓝,
 * 运行时 ThemeColorProvider 往 <html data-theme-color> 一写整站即换肤。
 * 明/暗用互斥选择器 `:root:not(.dark)[...]` 与 `:root.dark[...]`,不依赖源码顺序。
 *
 * 生成结果已提交入库,CI 无需运行本脚本。种子色或角色映射变更时手动重跑:
 *   node scripts/gen-themes.mjs   (脚本内部用 esbuild 打包以绕过 MCU v0.4 的 ESM
 *   相对导入缺 .js 扩展名 bug)
 */
import { fileURLToPath } from 'node:url';
