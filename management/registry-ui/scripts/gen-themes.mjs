function main() {
  // MCU v0.4 的内部相对导入缺 .js 扩展名,Node ESM 直接 import 会炸;用 esbuild
  // 把一个薄 worker 打成自包含 bundle 再跑,顺带在 worker 里做 argb→HSL 转换。
  const tmp = mkdtempSync(resolve(tmpdir(), 'hive-themes-'));
  const workerSrc = resolve(tmp, 'worker.mjs');
  const workerOut = resolve(tmp, 'worker.bundle.mjs');
  writeFileSync(workerSrc, WORKER());
  execFileSync(
    resolve(UI_ROOT, 'node_modules/.bin/esbuild'),
    [workerSrc, '--bundle', '--platform=node', '--format=esm', `--outfile=${workerOut}`, '--log-level=error'],
    { cwd: UI_ROOT },
  );
  const payload = JSON.stringify({ seeds: SEEDS, roleMap: ROLE_MAP });
  const json = execFileSync('node', [workerOut, payload], { cwd: UI_ROOT }).toString();
  const themes = JSON.parse(json);

  writeFileSync(OUT, renderCss(themes));
  rmSync(tmp, { recursive: true, force: true });
  console.log(`✓ wrote ${OUT} — ${SEEDS.length} themes × 2 modes`);
}

function renderCss(themes) {
  const header = `/* =========================================================================
   themes.css — GENERATED by scripts/gen-themes.mjs. DO NOT EDIT BY HAND.
   Material 3 role tokens for each Material Design palette seed, computed with
   @material/material-color-utilities (SchemeTonalSpot). Consumed by overriding
   the --md-* triplets that globals.css already aliases; runtime picker sets
   <html data-theme-color="<key>">. Light/dark use mutually-exclusive selectors
   so the cascade never depends on source order. Regenerate: node scripts/gen-themes.mjs
   ========================================================================= */\n`;
  const blocks = themes.map((t) => {
    const light = t.light.map(([k, v]) => `    ${k}: ${v};`).join('\n');
    const dark = t.dark.map(([k, v]) => `    ${k}: ${v};`).join('\n');
    return `:root:not(.dark)[data-theme-color='${t.key}'] {\n${light}\n  }\n\n:root.dark[data-theme-color='${t.key}'] {\n${dark}\n  }`;
  });
  return `${header}\n${blocks.join('\n\n')}\n`;
}

// ── worker：在 esbuild bundle 里跑,能安全 import MCU ────────────────────────
function WORKER() {
  return `
import { Hct, SchemeTonalSpot, MaterialDynamicColors, argbFromHex, hexFromArgb } from '@material/material-color-utilities';

const { seeds, roleMap } = JSON.parse(process.argv[2]);
const M = MaterialDynamicColors;

function hexToHslTriplet(hex) {
  const r = parseInt(hex.slice(1, 3), 16) / 255;
  const g = parseInt(hex.slice(3, 5), 16) / 255;
  const b = parseInt(hex.slice(5, 7), 16) / 255;
  const max = Math.max(r, g, b), min = Math.min(r, g, b);
  let h = 0, s = 0;
  const l = (max + min) / 2;
  const d = max - min;
  if (d !== 0) {
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r: h = (g - b) / d + (g < b ? 6 : 0); break;
      case g: h = (b - r) / d + 2; break;
      default: h = (r - g) / d + 4;
    }
    h /= 6;
  }
  const H = Math.round(h * 360);
  const S = Math.round(s * 1000) / 10;
  const L = Math.round(l * 1000) / 10;
  return H + ' ' + S + '% ' + L + '%';
}

function roleset(seedHex, isDark) {
  const scheme = new SchemeTonalSpot(Hct.fromInt(argbFromHex(seedHex)), isDark, 0);
  return roleMap.map(([cssVar, role]) => {
    const dc = M[role];
    const hex = hexFromArgb(dc.getArgb(scheme));
    // globals.css consumes these via hsl(var(--md-*)), so store bare triplets.
    return [cssVar, hexToHslTriplet(hex)];
  });
}

const out = seeds.map(({ key, seed }) => ({
  key,
  light: roleset(seed, false),
  dark: roleset(seed, true),
}));
process.stdout.write(JSON.stringify(out));
`;
}
